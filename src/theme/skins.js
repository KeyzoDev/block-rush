const SKIN_BY_ID = new Map(SKINS.map((skin) => [skin.id, skin]));

export function isSkinId(id) {
  return SKIN_BY_ID.has(id);
}

export function getSkin(id) {
  return SKIN_BY_ID.get(id) || SKIN_BY_ID.get(DEFAULT_SKIN_ID);
}

export function getSkinCssVariables(skinOrId) {
  const skin = typeof skinOrId === "string" ? getSkin(skinOrId) : skinOrId;
  const variables = {
    "--theme-bg-start": skin.ui.backgroundStart,
    "--theme-bg-mid": skin.ui.backgroundMid,
    "--theme-bg-end": skin.ui.backgroundEnd,
    "--theme-ambient": skin.ui.ambient,
    "--theme-hero-start": skin.ui.heroStart,
    "--theme-hero-mid": skin.ui.heroMid,
    "--theme-hero-end": skin.ui.heroEnd,
    "--theme-surface": skin.ui.surface,
    "--theme-surface-strong": skin.ui.surfaceStrong,
    "--theme-surface-muted": skin.ui.surfaceMuted,
    "--theme-text": skin.ui.text,
    "--theme-text-muted": skin.ui.textMuted,
    "--theme-text-on-color": skin.ui.textOnColor,
    "--theme-primary-start": skin.ui.primaryStart,
    "--theme-primary-mid": skin.ui.primaryMid,
    "--theme-primary-end": skin.ui.primaryEnd,
    "--theme-primary-text": skin.ui.primaryText,
    "--theme-accent": skin.ui.accent,
    "--theme-accent-soft": skin.ui.accentSoft,
    "--theme-board-frame": skin.ui.boardFrame,
    "--theme-board-start": skin.ui.boardStart,
    "--theme-board-end": skin.ui.boardEnd,
    "--theme-board-cell-start": skin.ui.boardCellStart,
    "--theme-board-cell-end": skin.ui.boardCellEnd,
    "--theme-shadow": skin.ui.shadow,
    "--theme-board": skin.board,
    "--theme-clear-glow": skin.visual.boardClear.glow,
    "--theme-clear-flash": skin.visual.boardClear.flash,
    "--theme-clear-sweep": skin.visual.boardClear.sweep,
    "--theme-clear-accent": skin.visual.boardClear.accent,
    "--theme-fever-glow": skin.visual.fever.glow,
    "--theme-fever-accent": skin.visual.fever.accent,
    "--theme-praise-0": skin.visual.praiseAccent[0],
    "--theme-praise-1": skin.visual.praiseAccent[1],
    "--theme-praise-2": skin.visual.praiseAccent[2],
  };

  skin.swatches.forEach((color, index) => {
    variables[`--skin-${index}`] = color;
  });

  return variables;
}
