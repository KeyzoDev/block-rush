function defineVisualPack(visual = {}) {
  return Object.freeze({
    ...DEFAULT_VISUAL_PACK,
    ...visual,
    previewPattern: Object.freeze([
      ...(visual.previewPattern || DEFAULT_VISUAL_PACK.previewPattern),
    ]),
    particle: Object.freeze({
      ...DEFAULT_VISUAL_PACK.particle,
      ...visual.particle,
      colors: Object.freeze([
        ...(visual.particle?.colors || DEFAULT_VISUAL_PACK.particle.colors),
      ]),
    }),
    boardClear: Object.freeze({
      ...DEFAULT_VISUAL_PACK.boardClear,
      ...visual.boardClear,
    }),
    fever: Object.freeze({
      ...DEFAULT_VISUAL_PACK.fever,
      ...visual.fever,
    }),
    praiseAccent: Object.freeze([
      ...(visual.praiseAccent || DEFAULT_VISUAL_PACK.praiseAccent),
    ]),
  });
}

function defineSkin(skin) {
  if (!skin?.id || !skin?.name || skin.swatches?.length < 4) {
    throw new Error("Every skin needs an id, name, and at least four block swatches.");
  }

  for (const token of REQUIRED_UI_TOKENS) {
    if (!skin.ui?.[token]) {
      throw new Error(`Skin "${skin.id}" is missing UI token "${token}".`);
    }
  }

  return Object.freeze({
    ...skin,
    swatches: Object.freeze([...skin.swatches]),
    ui: Object.freeze({ ...skin.ui }),
    visual: defineVisualPack(skin.visual),
  });
}

